export const getRandomPair = () => {
  const shuffled = [...videoData].sort(() => 0.5 - Math.random());
  return [shuffled[0], shuffled[1]];
};

export const getRandomSet = (count) => {
  const shuffled = [...videoData].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};
